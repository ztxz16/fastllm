#!/usr/bin/env python3
"""
Quick multimodal smoke test for a local Qwen3.5 model via transformers.

Examples:
  python test/multimodal/qwen3_5_transformers_check.py
  python test/multimodal/qwen3_5_transformers_check.py --image /path/to/image.png
  python test/multimodal/qwen3_5_transformers_check.py --device cpu --dtype float32
"""

import argparse
import os

from PIL import Image

from common import create_probe_image, print_banner


TEST_ROOT = os.path.dirname(os.path.abspath(__file__))
DEFAULT_MODEL_PATH = "/home/use/Qwen3.5-4B"
DEFAULT_IMAGE_PATH = os.path.join(TEST_ROOT, "qwen3_5_probe.png")
DEFAULT_PROMPT = (
    "Describe the image briefly. Mention the major shapes, their colors, "
    "and what is in the top-right area."
)


def parse_args():
    parser = argparse.ArgumentParser(
        description="Run a Qwen3.5 multimodal smoke test with transformers.",
    )
    parser.add_argument(
        "--model-path",
        default=DEFAULT_MODEL_PATH,
        help="Local Qwen3.5 model directory.",
    )
    parser.add_argument(
        "--image",
        default="",
        help="Image path for testing. If omitted, an autogenerated probe image is used.",
    )
    parser.add_argument(
        "--prompt",
        default=DEFAULT_PROMPT,
        help="Question passed together with the image.",
    )
    parser.add_argument(
        "--dtype",
        choices=["auto", "float16", "bfloat16", "float32"],
        default="auto",
        help="Torch dtype used during from_pretrained().",
    )
    parser.add_argument(
        "--device",
        default="",
        help="Single target device such as cpu or cuda:0. Empty means device_map=auto.",
    )
    parser.add_argument(
        "--max-new-tokens",
        type=int,
        default=128,
        help="Maximum number of newly generated tokens.",
    )
    return parser.parse_args()


def import_dependencies():
    try:
        import torch
        import transformers
    except ImportError as exc:
        raise RuntimeError(
            "This script requires torch, transformers, and pillow to be installed."
        ) from exc

    try:
        from transformers import AutoProcessor, Qwen3_5ForConditionalGeneration
    except ImportError as exc:
        raise RuntimeError(
            "The current transformers build does not expose Qwen3.5 support.\n"
            "Please install a recent version, for example:\n"
            '  pip install -U "transformers[serving] @ '
            'git+https://github.com/huggingface/transformers.git@main"'
        ) from exc

    return torch, transformers, AutoProcessor, Qwen3_5ForConditionalGeneration


def resolve_torch_dtype(torch_module, dtype_name):
    if dtype_name == "auto":
        return "auto"
    return getattr(torch_module, dtype_name)


def resolve_device_map(device):
    if device:
        return {"": device}
    return "auto"


def resolve_input_device(model, torch_module):
    model_device = getattr(model, "device", None)
    if model_device is not None and getattr(model_device, "type", None) != "meta":
        return model_device

    for parameter in model.parameters():
        if parameter.device.type != "meta":
            return parameter.device

    return torch_module.device("cpu")


def move_model_inputs(model_inputs, device):
    moved_inputs = {}
    # Keep grid metadata on CPU to avoid NVRTC reduction issues on some CUDA setups.
    cpu_only_keys = {"image_grid_thw", "video_grid_thw"}
    for key, value in model_inputs.items():
        if hasattr(value, "to") and key not in cpu_only_keys:
            moved_inputs[key] = value.to(device)
        else:
            moved_inputs[key] = value
    return moved_inputs


def ensure_probe_image(image_path):
    if image_path:
        if not os.path.exists(image_path):
            raise FileNotFoundError(f"Image not found: {image_path}")
        return image_path, False

    return create_probe_image(DEFAULT_IMAGE_PATH, label="Q35")


def build_messages(image_path, prompt):
    return [
        {
            "role": "system",
            "content": "You are a careful vision-language assistant. Answer concisely.",
        },
        {
            "role": "user",
            "content": [
                {"type": "image", "image": image_path},
                {"type": "text", "text": prompt},
            ],
        },
    ]


def main():
    args = parse_args()
    torch, transformers, AutoProcessor, Qwen3_5ForConditionalGeneration = (
        import_dependencies()
    )

    image_path, image_created = ensure_probe_image(args.image)
    messages = build_messages(image_path, args.prompt)
    torch_dtype = resolve_torch_dtype(torch, args.dtype)
    device_map = resolve_device_map(args.device)

    print_banner("Environment")
    print(f"torch: {torch.__version__}")
    print(f"transformers: {transformers.__version__}")
    print(f"cuda_available: {torch.cuda.is_available()}")
    if torch.cuda.is_available():
        print(f"cuda_device_0: {torch.cuda.get_device_name(0)}")

    print_banner("Request")
    print(f"model_path: {args.model_path}")
    print(f"image_path: {image_path}")
    print(f"image_created: {image_created}")
    print(f"prompt: {args.prompt}")
    print(f"dtype: {args.dtype}")
    print(f"device: {args.device or 'auto'}")
    print(f"max_new_tokens: {args.max_new_tokens}")
    if image_created:
        print(
            "autogenerated_probe: red square, blue circle, green triangle, "
            "yellow rectangle"
        )

    processor = AutoProcessor.from_pretrained(args.model_path)
    model = Qwen3_5ForConditionalGeneration.from_pretrained(
        args.model_path,
        torch_dtype=torch_dtype,
        device_map=device_map,
        low_cpu_mem_usage=True,
    )
    input_device = resolve_input_device(model, torch)
    print(f"input_device: {input_device}")

    prompt_text = processor.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=True,
        enable_thinking=False,
    )

    with Image.open(image_path) as image:
        image = image.convert("RGB")
        model_inputs = processor(
            text=[prompt_text],
            images=[image],
            padding=True,
            return_tensors="pt",
        )

    model_inputs = move_model_inputs(model_inputs, input_device)

    with torch.inference_mode():
        generated_ids = model.generate(
            **model_inputs,
            max_new_tokens=args.max_new_tokens,
            do_sample=False,
            use_cache=True,
        )

    completion_ids = generated_ids[:, model_inputs["input_ids"].shape[1] :]
    response = processor.batch_decode(
        completion_ids,
        skip_special_tokens=True,
        clean_up_tokenization_spaces=False,
    )[0].strip()

    print_banner("Response")
    print(response or "<empty response>")


if __name__ == "__main__":
    main()
